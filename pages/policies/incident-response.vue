            <li>“There is a policy violation concerning the entry door ”</li>
          </ul>
        </li>
      </ul>
      <p>The purpose of these strict parameters is so that the most meaningful information can be collected quickly without any extraneous, unverified or potentially inaccurate details. This is purely about objective observation.</p>
      <h2>2. Assessment</h2>
      <p>Once the responsible party has observed the details of the violation, they can then assess the following:</p>
      <ul>
        <li>
          <p><b>Which policy is being violated? </b></p>
          <p>This is strictly a parameter to identify the terms of the violation, for example:</p>
          <ul>
            <li>“There is a policy violation concerning Health & Safety” </li>
            <li>“There is a policy violation concerning Privacy and Safeguarding”</li>
          </ul>
        </li>
      </ul>
      <p>The purpose of this strict parameter is so that there is a clear context around which potential resolution can be formed which is appropriate to the severity of the violation and fair to the parties involved.</p>
