      <p>When the responsible party observes any event or occurrence that conflicts with the terms of the participation policies, they are to note:</p>
      <ul>
        <li>
          <p><b>Who is involved?</b></p>
          <p>This is strictly a parameter to identify the person or persons involved, whether they be active or passive is purely incidental at this point, for example:</p>
