        <li>Week mentors are responsible for themselves and the participants in their hub.</li>
        <li>Weekend volunteers are responsible for themselves and the participants taking part at the weekend.</li>
      </ul>
      <p>These responsible parties have a duty of care to implement this policy, and where this falls outside of their individually assigned remit, to ensure that the appropriate person takes responsibility.</p>
      <p>Please note - unlike our other policies, we do not place any expectation upon the young people attending our events to respond to incidents other than to appropriately protect themselves.</p>
      <h2>Definitions</h2>
      <p>In the BASH Festival of Code, we define an “incident” as any occurrence or situation that violates the terms of the following “participation” policies:</p>
      <ul>
