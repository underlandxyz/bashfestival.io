      <p>At each level in the operational hierarchy of responsible parties, incidents are to be reported to the next level of oversight. Logs of activity and incidents are collected daily from all hub sites and examined by Underland alongside whatever interim or definitive resolution may have been actioned, and appropriate additional guidance developed from this feedback will be disseminated accordingly. </p>
