            <li>Within the venue, all spaces and utilities that will be accessible by participants are exclusively for their use for the duration of the weekend's events.</li>
            <li>Access points for entry/exit will be staffed at all times by authorised personnel to ensure an accurate register of whereabouts at all times.</li>
          </ul>
        </li>
        <li>
          <b>Sleeping Spaces</b>
          <p>Each sleeping space will be monitored by its own register, with only those young people present on that space's register being granted access to come and go.</p>
          <p>Mixed-gender sleeping spaces will be supervised by multiple DBS-checked adults at all times.</p>
        </li>
        <li>
          <b>Access to quiet spaces</b>
          <p>Young people affected by hypersensory conditions will be afforded access to designated areas such that they may more comfortably manage their experience of the event </p>
        </li>
