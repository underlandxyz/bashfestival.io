        <li>Hub Leaders</li>
        <li>Week Mentors</li>
        <li>Weekend Volunteers</li>
      </ul>
      <p>These responsible parties have a duty of care to implement this policy, and where this falls outside of their individually assigned remit, ensure that the appropriate responsible person takes responsibility.</p>
      <h2>Measures Prior to the event</h2>
      <ul>
        <li>
          <b>Disclosure and Barring Service (DBS) Checks</b>
          <p>As a cornerstone of our commitment to ensuring that any and all volunteers taking part in the festival are appropriately suited to the responsibility of working with young people, every volunteer will be required to pass a new Enhanced DBS check, or provide details for us to verify an existing check using the DBS Update Service.</p>
        </li>
        <li>
          <b>Permission from a parent of guardian</b>
          <p>Any child wishing to take part in the festival must have their attendance explicitly supported by a parent or guardian, and said parent or guardian will be involved in all pertinent ticketing and attendance correspondence.</p>
        </li>
        <li>
          <b>Training</b>
          <p>All Hub Leaders will receive training to ensure that they can provide their duty of care in accordance with the stated terms of this Safeguarding Policy. This also includes training around our Incident Response Policy (we're still writing this), working with young people who are on the autism spectrum, and how to spot and report abuse.</p>
        </li>
      </ul>
      <h2>Measures During the Festival Week (Monday - Friday)</h2>
      <ul>
        <li>
          <b>Regional Hub Venues</b>
          <p>Venues designated as work spaces for hubs must be subject to an advance survey by Hub Leaders to ensure they comply with our Health & Safety Policy (currently being finalised) and that they meet the following security requirements:</p>
          <ul>
            <li>venues must be enclosed spaces with access control for authorised people only</li>
            <li>where they are part of a larger space, venues will be appropriately secured and closed off from access by unauthorised people</li>
            <li>proposed venue assessments must be submitted by Hub Leaders and approved by the Regional Hub Coordinator prior to the BASH Festival of Code week</li>
          </ul>
        </li>
        <li>
          <b>DBS Checked Adults</b>
          <p>Work spaces will be attended by multiple DBS checked adults at all times. We will work with each Hub Leader to ensure there is a contingency plan to ensure that there is as even a spread of personnel on hand to remove any eventuality of lone-working or interruption of work sessions until cover supervision can be sourced.</p>
        </li>
        <li>
          <b>Medical & Accessibility Requirements</b>
          <p>Each young person's medical and accessibility requirements, and parent/guardian contact information, will be collated at the ticketing stage and made available only to Hub Leaders and core BASH Festival team members. More details about your data are in our <n-link to='/policies/privacy'>Privacy Policy</n-link>.</p>
        </li>
        <li>
          <b>Attendance of Parents and Guardians</b>
          <p>While we discourage parents/guardians from attending in most cases, if there are any cases where a young person requires the presence of their parent/guardian then this need will be assessed on a case by case basis and accommodated where feasible.</p>
        </li>
        <li>
          <b>Code of Conduct</b>
          <p>Any and all participants, Hub Leaders, Mentors, Volunteers and BASH Festival of Code team members must agree and abide by our <n-link to='/policies/conduct'>Code of Conduct</n-link>.</p>
          <p>In the event of a Code of Conduct violation by any parties, reports can be made via email, Twitter Direct Message, telephone or anonymously online. Reports will be restricted to core BASH Festival team members for investigation. Details about reporting are in our <n-link to='/policies/conduct'>Code of Conduct</n-link></p>
        </li>
      </ul>
